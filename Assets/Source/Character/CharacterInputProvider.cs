        public void OnInput(NetworkRunner runner, Fusion.NetworkInput input)
        {
            var characterInput = new NetworkInput();

            characterInput.Vertical = Input.GetAxis("Vertical");
            characterInput.Horizontal = Input.GetAxis("Horizontal");
            characterInput.Jump = Input.GetKey(KeyCode.Space);
            
            input.Set(characterInput);
        }
        
